import csv

class Student:
    name = None
    subjects = {}

    def __init__(self, name: str, subjects_file: str):
        '''–ü—Ä–∏–Ω–∏–º–∞–µ—Ç –∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞ –∏ —Ñ–∞–π–ª —Å –ø—Ä–µ–¥–º–µ—Ç–∞–º–∏ –∏ –∏—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏'''
        self.name = name
        self.load_subjects(subjects_file)

    def __getattr__(self, name: str):
        '''–ü–æ–∑–≤–æ–ª—è–µ—Ç –ø–æ–ª—É—á–∞—Ç—å –∑–Ω–∞—á–µ–Ω–∏—è –∞—Ç—Ä–∏–±—É—Ç–æ–≤ –ø—Ä–µ–¥–º–µ—Ç–æ–≤ (–æ—Ü–µ–Ω–æ–∫ –∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ —Ç–µ—Å—Ç–æ–≤) –ø–æ –∏—Ö –∏–º–µ–Ω–∞–º'''
        if not name in self.subjects.keys():
            raise ValueError(f"–ü—Ä–µ–¥–º–µ—Ç {name} –Ω–µ –Ω–∞–π–¥–µ–Ω")
        return self.subjects[name]

    def __setattr__(self, name: str, value: str):
        '''–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —É—Å—Ç–∞–Ω–æ–≤–∫—É –∞—Ç—Ä–∏–±—É—Ç–∞ name

         –£–±–µ–∂–¥–∞–µ—Ç—Å—è, —á—Ç–æ name –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã –∏ —Å–æ—Å—Ç–æ–∏—Ç —Ç–æ–ª—å–∫–æ –∏–∑ –±—É–∫–≤'''
        if name == 'name':
            if not (isinstance(value, str) and ''.join(value.split()).isalpha() and value.istitle()):
                raise ValueError(f'–§–ò–û –¥–æ–ª–∂–Ω–æ —Å–æ—Å—Ç–æ—è—Ç—å —Ç–æ–ª—å–∫–æ –∏–∑ –±—É–∫–≤ –∏ –Ω–∞—á–∏–Ω–∞—Ç—å—Å—è —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã')
            return object.__setattr__(self, name, value)

    def __str__(self):
        '''–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫–æ–≤–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —Å—Ç—É–¥–µ–Ω—Ç–∞, –≤–∫–ª—é—á–∞—è –∏–º—è –∏ —Å–ø–∏—Å–æ–∫ –ø—Ä–µ–¥–º–µ—Ç–æ–≤.

        –°—Ç—É–¥–µ–Ω—Ç: –ò–≤–∞–Ω –ò–≤–∞–Ω–æ–≤
        –ü—Ä–µ–¥–º–µ—Ç—ã: –ú–∞—Ç–µ–º–∞—Ç–∏–∫–∞, –ò—Å—Ç–æ—Ä–∏—è'''
        subjects = ', '.join([key for key, value in self.subjects.items()
                              if value['grade'] or value['test_score']])
        return f'–°—Ç—É–¥–µ–Ω—Ç: {self.name}\n' \
               f'–ü—Ä–µ–¥–º–µ—Ç—ã: {subjects}'

    def load_subjects(self, subjects_file):
        '''–ó–∞–≥—Ä—É–∂–∞–µ—Ç –ø—Ä–µ–¥–º–µ—Ç—ã –∏–∑ —Ñ–∞–π–ª–∞ CSV

        –ò—Å–ø–æ–ª—å–∑—É–µ—Ç –º–æ–¥—É–ª—å csv –¥–ª—è —á—Ç–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –∏–∑ —Ñ–∞–π–ª–∞ –∏ –¥–æ–±–∞–≤–ª—è–µ—Ç –ø—Ä–µ–¥–º–µ—Ç—ã –≤ –∞—Ç—Ä–∏–±—É—Ç subjects'''
        with open(subjects_file, 'r', encoding='utf-8') as f:
            reader = csv.DictReader(f, quoting=csv.QUOTE_MINIMAL)
            for field in reader.fieldnames:
                self.subjects[field] = \
                    {
                        'grade': [],
                        'test_score': []
                    }

    def add_grade(self, subject, grade):
        '''–î–æ–±–∞–≤–ª—è–µ—Ç –æ—Ü–µ–Ω–∫—É –ø–æ –∑–∞–¥–∞–Ω–Ω–æ–º—É –ø—Ä–µ–¥–º–µ—Ç—É

        –£–±–µ–∂–¥–∞–µ—Ç—Å—è, —á—Ç–æ –æ—Ü–µ–Ω–∫–∞ —è–≤–ª—è–µ—Ç—Å—è —Ü–µ–ª—ã–º —á–∏—Å–ª–æ–º –æ—Ç 2 –¥–æ 5'''
        if not 2 <= grade <= 5:
            raise ValueError('–û—Ü–µ–Ω–∫–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å —Ü–µ–ª—ã–º —á–∏—Å–ª–æ–º –æ—Ç 2 –¥–æ 5')
        self.subjects[subject]['grade'].append(grade)

    def add_test_score(self, subject, test_score):
        '''–î–æ–±–∞–≤–ª—è–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ç–µ—Å—Ç–∞ –ø–æ –∑–∞–¥–∞–Ω–Ω–æ–º—É –ø—Ä–µ–¥–º–µ—Ç—É

        –£–±–µ–∂–¥–∞–µ—Ç—Å—è, —á—Ç–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ç–µ—Å—Ç–∞ —è–≤–ª—è–µ—Ç—Å—è —Ü–µ–ª—ã–º —á–∏—Å–ª–æ–º –æ—Ç 0 –¥–æ 100'''
        if not 0 <= test_score <= 100:
            raise ValueError('–†–µ–∑—É–ª—å—Ç–∞—Ç —Ç–µ—Å—Ç–∞ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —Ü–µ–ª—ã–º —á–∏—Å–ª–æ–º –æ—Ç 0 –¥–æ 100')
        self.subjects[subject]['test_score'].append(test_score)

    def get_average_grade(self):
        '''–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª –ø–æ –≤—Å–µ–º –ø—Ä–µ–¥–º–µ—Ç–∞–º'''
        grades = [grade for value in self.subjects.values() for grade in value['grade']]
        return sum(grades) / len(grades)

    def get_average_test_score(self, subject):
        '''–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª –ø–æ —Ç–µ—Å—Ç–∞–º –¥–ª—è –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –ø—Ä–µ–¥–º–µ—Ç–∞'''
        test_score = getattr(self, subject)['test_score']
        return sum(test_score) / len(test_score)


