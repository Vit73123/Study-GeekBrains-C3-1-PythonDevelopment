# Нарисовать в консоли ёлку спросив у пользователя количество рядов.
# Пример результата:

# Сколько рядов у ёлки? 5
#     *
#    ***
#   *****
#  *******
# *********

rows = int(input('Количество рядов: '))
for i in range(0, rows):
    points = int(i * 2 + 1)
    spaces = (rows - 1) - i
    print(' ' * spaces, '*' * points)
